    private void loadList(ArrayList<Adventurer> adventurersList) {
        DefaultListModel defaultListModel = new DefaultListModel<>();
        for (Adventurer adventurer : adventurersList) {
            defaultListModel.addElement(adventurer.toString());
        }
        lstAdventurers.setModel(defaultListModel);
    }

